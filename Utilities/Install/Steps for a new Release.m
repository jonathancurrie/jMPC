%% Steps for a new Release (my use only..)

% Local
% 1) Update jMPCver version, release notes and date
% 2) Rebuild MEX files
% 3) Run jMPC_package to update Contents file

% Git
% 1) Commit the latest changes with the new version
% 2) Tag the release using the format "jMPC_Toolbox_vX.XX_Released"
% 3) Push the changes to Github
% 4) Under jMPC/Releases, draft a new release. Select the tag, title "jMPC Toolbox vX.XX".

% Website
% 1) Update jMPC Wiki page with new features under news